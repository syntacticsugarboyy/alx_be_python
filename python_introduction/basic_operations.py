number1 = 5
number2 = 3

some = 'Addition'
difference = 'Subtraction'
product = 'Multiplication'

print ('{} of {:d} and {:d} is {:d}'. format(some, number1, number2, (number1 + number2)))
print ('{} of {:d} and {:d} is {:d}'. format(difference, number1, number2, (number1 - number2)))
print ('{} of {:d} and {:d} is {:d}'. format(product, number1, number2, (number1 * number2)))
